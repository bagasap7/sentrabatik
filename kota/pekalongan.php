        <p>Sejarah motif batik Pekalongan Sejarah motif batik pekalongan tercatat banyak dipengaruhi oleh budaya dari luar yaitu budaya dari Arab (Islam) dan budaya dari tionghoa yang terjadi pada masa lalu. Sebelumnya, mayoritas masyarakat Pekalongan selalu membuat motif batik dengan meniru antara satu dan lainnya. Jadi sebenarnya masyarakat Pekalongan memiliki imajinasi dan kreatifitas yang tinggi, mengapa? Karena masyarakat Pekalongan dituntut untuk bisa menciptakan motif batik pekalongan terbaru agar tidak disamai oleh yang lainnya.</p>
