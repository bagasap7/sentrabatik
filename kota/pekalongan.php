          <strong>
            Beberapa ciri khasnya </strong>
        </h4>
        <p>Setiap batik memiliki ciri khasnya masing-masing sesuai dengan asalnya sehingga orang yang menggunakan batik tersebut bisa lebih mudah mengenali dan mengetahui asal dari batik tersebut. Sama halnya seperti motif batik pekalongan yang memiliki ciri khasnya sendiri baik pada pola, motif, dan lainnya. ciri khas batik pekalongan yaitu:</p>
        <h6 style="color: #a87a41; font-family: 'Spectral', serif">
          <strong>
            1. Memiliki warna yang cerah </strong>
        </h6>
        <p>Salah satu ciri batik pekalongan yaitu memiliki warna yang sangat cerah. Salah satu warna khas dari batik pekalongan memiliki warna batik pesisir dengan warna yang lebih cerah dan terang. Umumnya warna-warna yang digunakan dalam proses pewarnaan batik pekalongan yaitu warna pink terang, merah muda, hijau, kuning, biru, dalem dan jenis-jenis warna lainnya yang sering dijumpai di sekitar kita.</p>
        <h6 style="color: #a87a41; font-family: 'Spectral', serif">
          <strong>
            2. Memiliki motif penuh dengan titik dan garis </strong>
        </h6>
        <p>Setiap batik pekalongan memiliki motif titik dan juga garis pada setiap hasilnya. Misalnya jika batik memiliki pola bunga kecil-kecil maka akan diapit dan dikelilingi dengan motif-motif perpaduan titik dan garis sehingga dapat memberikan desain tampilan motif yang lebih hidup dan nyata. Jadi motif batik pekalongan memiliki motif batik yang sangat ramai, padat, dan juga warna-warna yang cerah sehingga dapat memberikan daya tariknya tersendiri.</p>
        <h6 style="color: #a87a41; font-family: 'Spectral', serif" ;>
          <strong>
            3. Memiliki motif jlamprang </strong>
        </h6>
        <p>Motif jlamprang merupakan motif titik geometris yang serupa dari ujung ke ujung. Motif ini menggunakan perpaduan antara warna-warna yang cerah agar lebih menarik.</p>
        <h6 style="color: #a87a41; font-family: 'Spectral', serif">
          <strong>
            4. Memiliki motif khas keturunan Tiongkok </strong>
        </h6>
        <p>Batik pekalongan juga memiliki motif khas Tiongkok misalnya seperti motif bergambar burung phoenix, naga, dan motif lainnya.</p>
        <h6 style="color: #a87a41; font-family: 'Spectral', serif">
          <strong>
            5. Mayoritas bermotif bunga </strong>
        </h6>
